final=''
word= str(input())
for letter in word:
    if letter=='i':
        final+='!'
    elif letter=='a':
        final+='@'
    elif letter=='m':
        final+='M'
    elif letter=='B':
        final+='8'
    elif letter=='o':
        final+='.'
    else:
        final+= letter
print(final+'q*s')



